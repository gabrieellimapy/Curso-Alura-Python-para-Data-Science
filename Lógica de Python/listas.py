# É possível criar uma lista com valores já definidos

idades = [10,20,30,40,50] #índices de procura nas listas
#          0  1  2  3  4
#          0 -4 -3 -2 -1
type(idades) #apurador de tipos

# -------- Para consultar valores em listas, vejamos o exemplo abaixo -------- #
idades[2]
# -- Para mostrar vários elementos dentro de uma lista determinando posições - #
idades[0:3]
# ----- Para mostrar listas até o final dentro de uma determinada posicao ---- #
idades[1:]
