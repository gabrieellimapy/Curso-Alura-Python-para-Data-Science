# ------------------------- Considere os dados abaixo ------------------------ #
dados = [ 
    ['A', 'B', 'C'],
    ['D', 'E', 'F'],
    ['G', 'H', 'I']
]

# -------------------------- Avalie os itens abaixo: ------------------------- #

result = []
for lista in dados:
    for item in lista:
        result.append(item)
result

# ----------------------------- Saída de dados = ----------------------------- #
# --------------- ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I'] -------------- #

result_2 = []
for lista in dados:
    result_2 += lista
result_2

# ----------------------------- Saída de dados = ----------------------------- #
# --------------- ['A', 'B', 'C', 'D', 'E', 'F', 'G', 'H', 'I'] -------------- #

[item for lista in dados for item in lista]

